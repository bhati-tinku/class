<p>Realme has just introduced three new 5G phones to the Chinese audience - the premium <a href="realme_x7-10408.php">Realme X7</a>, <a href="realme_x7_pro-10380.php">Realme X7 Pro</a>, and the ultra-affordable <a href="realme_v3-10409.php">Realme V3</a>. The vanilla Realme X7 is the official debut of the Mediatek Dimensity 800U chipset, so we'll start with that one.</p>

<img class="inline-image" width="727" height="454" src="https://fdn.gsmarena.com/imgroot/news/20/09/realme-x7-x7pro-v3-5g-ofic/-727/gsmarena_000.jpg" alt="Realme X7 and Realme X7 Pro are official, Realme V3 tags along">

<h3>Realme X7</h3>

<p>The chipset of the Realme X7 is built on the 7nm process technology and its two  big  Cortex-A76 CPU cores run at 2.4 GHz. The GPU is Mali-G57 and the platform is coupled with 6GB or 8GB LPDDR4X RAM, while the internal storage is 128GB.</p>

<p>The front is occupied by a 6.4â AMOLED panel with 180 Hz touch sampling rate. It has a Full HD+ resolution and a single punch hole in the top left corner for the 32MP f/2.5 selfie camera.</p>

<img class="inline-image" width="727" height="313" src="https://fdn.gsmarena.com/imgroot/news/20/09/realme-x7-x7pro-v3-5g-ofic/-727/gsmarena_004.jpg" alt="Realme X7 and Realme X7 Pro are official, Realme V3 tags along">

<p>There are four more shooters on the back - 64MP f/1.8 main + 8 MP f/2.3 ultrawide + 2 MP B/W sensor for portrait shots + 2 MP macro cam. They are lined up in a rectangular setup on the back, having a single LED flash keeping them company.</p>

<p>There is no fingerprint scanner on the side or the back - it is embedded under the OLED panel. The Realme X7 comes with 65W fast charging for the 4,300 mAh battery through a USB-C.</p>

<img class="inline-image" width="727" height="454" src="https://fdn.gsmarena.com/imgroot/news/20/09/realme-x7-x7pro-v3-5g-ofic/-727/gsmarena_005.jpg" alt="Realme X7 and Realme X7 Pro are official, Realme V3 tags along">

<p>Realme X7 weighs just 175 grams and is about 8.1 mm thin. It has two slots for SIM cards and they both can hook up to 5G networks - the Dimensity 800U chipset allows easy switch between them - that way one card can be used for calls and the other for fast browsing and games.</p>

<p>The phone is offered in Blue, White, and gradient color with Dare to Leap writing on the back. Price starts from CNY1,799 ($265/â¬220) and the first flash sale is scheduled for September 7.</p>

<h3>Realme X7 Pro</h3>

<p>The <a href="realme_x7_pro-10380.php">Realme X7 Pro</a> comes with a 6.55â AMOLED of 120Hz refresh rate and 240Hz touch sampling rate. It still comes with a punch hole in the upper left corner for the 32MP front-facing camera.</p>

<img class="inline-image" width="727" height="313" src="https://fdn.gsmarena.com/imgroot/news/20/09/realme-x7-x7pro-v3-5g-ofic/-727/gsmarena_002.jpg" alt="Realme X7 and Realme X7 Pro are official, Realme V3 tags along">

<p>The chipset in the X7 Pro is Dimensity 1000+, which is the current MediaTek top SoC and is quite close to even Qualcomm's Snapdragon 865 in terms of performance. The phone comes with a fancy heat dissipation system, including a vapor chamber, to lower the core temperature by up to 10 degrees Celsius.</p>

<p>Proper cooling is also needed during fast charging. Just like the Realme X7, the Pro phone has 65W SuperDart Flash Charge, but the power cell is slightly bigger - 4,500 mAh. One thing Realme didnât change is the camera combo - it remains 64 MP + 8 MP + 2 MP + 2 MP.</p>

<img class="inline-image" width="727" height="454" src="https://fdn.gsmarena.com/imgroot/news/20/09/realme-x7-x7pro-v3-5g-ofic/-727/gsmarena_003.jpg" alt="Realme X7 and Realme X7 Pro are official, Realme V3 tags along">

<p>There are three prices for the Realme X7 Pro - CNY2,199 ($320/â¬270) for the 6/128GB option, CNY2,399 ($350/â¬295) if you want 8GB RAM with the same storage or CNY3,199 ($470/â¬390) for an 8/256GB variant.</p>

<p>The X7 Pro is available for pre-order, with first shipments scheduled for next week, September 7. The version with the âDare to Leapâ slogan is still here, but you can also get it in Dark Blue or Pearl White.</p>

<h3>Realme V3 5G</h3>

<p>The Realme V3 5G is the  most affordable 5G phone to date. It starts at just just CNY999 ($146/â¬122)and comes with a Dimensity 720 chipset by Mediatek.</p>

<p>The V3 is built around a 6.5â LCD of HD+ resolution with a waterdrop notch for the 8MP selfie camera. The fingerprint scanner made its way on the back where three cameras and an LED flash are lined up in a square.</p>

<img class="inline-image" width="727" height="365" src="https://fdn.gsmarena.com/imgroot/news/20/09/realme-x7-x7pro-v3-5g-ofic/-727/gsmarena_008.jpg" alt="Realme X7 and Realme X7 Pro are official, Realme V3 tags along">

<p>The main camera is 13 MP with f/2.2 aperture and is accompanied by two 2 MP f/2.4 cams - one for close-up shots and the other for fancier portrait shots with artificial blur around the object.</p>

<p>Realme V3 5G comes with a relatively big 5,000 mAh battery that supports 18W charging through the USB-C. There is also a 3.5 mm audio jack right next to the USB port.</p>

